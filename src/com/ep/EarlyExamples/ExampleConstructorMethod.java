package com.ep.EarlyExamples;

public class ExampleConstructorMethod {
    //Constructor Method
    public ExampleConstructorMethod(){
        System.out.println("This is constructor method");
    }

    //Constructor Method with parameter
    public ExampleConstructorMethod(String param){
        System.out.println("This is a constructor method with a paramter");
    }

    //Instance Method
    public void InstanceMethod(){
        System.out.println("This is an instance method");
    }
}
